#include "libft.h"

void	bubble_sort(size_t *arr, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (i < n - 1)
	{
		j = 0;
		while (j < n - i - 1)
		{
			if (arr[j] > arr[j + 1])
				u_swap(&arr[j], &arr[j + 1]);
			j++;
		}
		i++;
	}
}
