#include "libft.h"

/*
** Swap the two signed numbers (a) and (b).
*/

void	swap(ssize_t *a, ssize_t *b)
{
	ssize_t	cache;

	if (a && b)
	{
		cache = *a;
		*a = *b;
		*b = cache;
	}
}
