#include "libft.h"

/*
** Swap the two numbers (a) and (b).
*/

void	u_swap(size_t *a, size_t *b)
{
	size_t	cache;

	if (a && b)
	{
		cache = *a;
		*a = *b;
		*b = cache;
	}
}
